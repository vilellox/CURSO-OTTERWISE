/* Crie um algoritmo que tem como entrada um array de notas, e imprime no
console a média aritmética dessas notas.
Exemplo Entrada:
[6, 8, 7]
Exemplo Saída:
7
*/

let notas = [6, 8, 7];
let soma = 0;
let media = 0;

for (let index = 0; index <= notas[index]; index++) {
    
    soma += notas[index]
    media = soma / notas.length
}

console.log(media)